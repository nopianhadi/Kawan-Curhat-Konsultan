// Helper function untuk load sample data
export const loadSampleData = (contentType: string) => {
  switch (contentType) {
    case 'blog':
      return sampleBlogPosts;
    case 'services':
      return sampleServices;
    case 'testimonials':
      return sampleTestimonials;
    case 'videos':
      return sampleVideoTestimonials;
    case 'faq':
      return sampleFAQs;
    case 'gallery':
      return sampleGallery;
    default:
      return [];
  }
};

// Helper function untuk save ke localStorage
export const initializeSampleData = () => {
  const dataTypes = ['blog', 'services', 'testimonials', 'videos', 'faq', 'gallery'];
  
  dataTypes.forEach(type => {
    const storageKey = `admin_${type}_data`;
    const existingData = localStorage.getItem(storageKey);
    
    // Hanya load sample data jika belum ada data
    if (!existingData) {
      const sampleData = loadSampleData(type);
      localStorage.setItem(storageKey, JSON.stringify(sampleData));
    }
  });
};
